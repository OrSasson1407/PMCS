import Redis from 'ioredis';

const REDIS_URL = process.env.REDIS_URL || 'redis://localhost:6379';

// ?? Redis Client ??????????????????????????????????????????????????????????????
export const redis = new Redis(REDIS_URL, {
  maxRetriesPerRequest: 3,
  retryStrategy(times) {
    const delay = Math.min(times * 100, 3000);
    console.warn(`[redis] Retrying connection attempt ${times} in ${delay}ms...`);
    return delay;
  },
});

redis.on('connect', () => {
  console.log('[redis] Connected successfully');
});

redis.on('error', (err) => {
  console.error('[redis] Connection error:', err);
});

// ?? Helper: set a key with optional TTL (seconds) ????????????????????????????
export const setCache = async (
  key: string,
  value: unknown,
  ttlSeconds?: number
): Promise<void> => {
  const serialized = JSON.stringify(value);
  if (ttlSeconds) {
    await redis.set(key, serialized, 'EX', ttlSeconds);
  } else {
    await redis.set(key, serialized);
  }
};

// ?? Helper: get and deserialize a cached value ???????????????????????????????
export const getCache = async <T>(key: string): Promise<T | null> => {
  const data = await redis.get(key);
  if (!data) return null;
  return JSON.parse(data) as T;
};

// ?? Helper: delete a cached key ???????????????????????????????????????????????
export const deleteCache = async (key: string): Promise<void> => {
  await redis.del(key);
};

// ?? Health check ??????????????????????????????????????????????????????????????
export const checkRedisConnection = async (): Promise<void> => {
  await redis.ping();
  console.log('[redis] PING successful');
};
