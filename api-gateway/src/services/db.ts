import { Pool, PoolClient } from 'pg';

const DATABASE_URL = process.env.DATABASE_URL || 'postgresql://pmcs:pmcs@localhost:5432/pmcs';

// ?? Connection Pool ???????????????????????????????????????????????????????????
export const pool = new Pool({
  connectionString: DATABASE_URL,
  max: 20,
  idleTimeoutMillis: 30000,
  connectionTimeoutMillis: 2000,
});

pool.on('error', (err) => {
  console.error('[db] Unexpected error on idle client:', err);
  process.exit(-1);
});

// ?? Helper: run a query via the pool ?????????????????????????????????????????
export const query = async (text: string, params?: unknown[]) => {
  const start = Date.now();
  const result = await pool.query(text, params);
  const duration = Date.now() - start;
  console.log(`[db] query executed in ${duration}ms | rows: ${result.rowCount}`);
  return result;
};

// ?? Helper: get a dedicated client for transactions ???????????????????????????
export const getClient = async (): Promise<PoolClient> => {
  const client = await pool.connect();
  return client;
};

// ?? Health check ??????????????????????????????????????????????????????????????
export const checkDbConnection = async (): Promise<void> => {
  const result = await query('SELECT NOW()');
  console.log(`[db] Connected. Server time: ${result.rows[0].now}`);
};
