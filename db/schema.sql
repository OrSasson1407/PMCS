-- PMCS PostgreSQL Schema
-- Auto-executed on first postgres container boot via docker-compose volume mount

CREATE EXTENSION IF NOT EXISTS "pgcrypto";

-- ?? organizations ?????????????????????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS organizations (
  id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  name             VARCHAR(255) NOT NULL,
  created_at       TIMESTAMP   DEFAULT NOW(),
  risk_tolerance   INTEGER     DEFAULT 80
);

-- ?? repositories ?????????????????????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS repositories (
  id              UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
  org_id          UUID         NOT NULL REFERENCES organizations(id) ON DELETE CASCADE,
  vcs_id          VARCHAR(100) NOT NULL UNIQUE,
  name            VARCHAR(255) NOT NULL,
  default_branch  VARCHAR(100) DEFAULT 'main'
);

-- ?? branches ??????????????????????????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS branches (
  id             UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
  repo_id        UUID         NOT NULL REFERENCES repositories(id) ON DELETE CASCADE,
  name           VARCHAR(255) NOT NULL,
  latest_commit  VARCHAR(40)  NOT NULL,
  last_updated   TIMESTAMP    NOT NULL,
  is_merged      BOOLEAN      DEFAULT false
);

-- ?? ast_nodes (high write volume) ????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS ast_nodes (
  id              UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
  branch_id       UUID         NOT NULL REFERENCES branches(id) ON DELETE CASCADE,
  file_path       VARCHAR(500) NOT NULL,
  node_signature  TEXT         NOT NULL,
  ast_hash        VARCHAR(64)  NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_ast_nodes_file_path ON ast_nodes(file_path);
CREATE INDEX IF NOT EXISTS idx_ast_nodes_branch_id ON ast_nodes(branch_id);

-- ?? risk_events ???????????????????????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS risk_events (
  id                UUID           PRIMARY KEY DEFAULT gen_random_uuid(),
  branch_a_id       UUID           NOT NULL REFERENCES branches(id),
  branch_b_id       UUID           NOT NULL REFERENCES branches(id),
  probability_score DECIMAL(3,2)   NOT NULL CHECK (probability_score BETWEEN 0.00 AND 1.00),
  status            VARCHAR(50)    NOT NULL DEFAULT 'OPEN'
                                   CHECK (status IN ('OPEN', 'RESOLVED_MANUAL', 'RESOLVED_AI')),
  created_at        TIMESTAMP      DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_risk_events_status ON risk_events(status);
